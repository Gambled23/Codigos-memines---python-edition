edad = 19
nombre = "pablo"

#1ra forma, con el print y concatenando las cadenas como en java 
print("Hola",nombre,"tienes",edad,"anios")

#2da forma, con la funcion format
print("hola {} tienes {} anios".format(nombre, edad))

#3ra forma, usando el f al inicio y las variables entre corchetes, es de las mas sencillas
print(f"Hola {nombre} tienes {edad} anios")

